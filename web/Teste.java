public class Teste {
    
private int idCerveja;
    private double investido;
    private double lucroEstimado;

    public Teste() {
        this.idCerveja = 0;
        this.investido = 0;
        this.lucroEstimado = 0;
    }

    public int getIdCerveja() {
        return idCerveja;
    }

    public void setIdCerveja(int idCerveja) {
        this.idCerveja = idCerveja;
    }

    public double getInvestido() {
        return investido;
    }

    public void setInvestido(double investido) {
        this.investido = investido;
    }

    public double getLucroEstimado() {
        return lucroEstimado;
    }

    public void setLucroEstimado(double lucroEstimado) {
        this.lucroEstimado = lucroEstimado;
    }
    
    
}
